        /// <summary>
        /// Converts an AD (Gregorian) date to its BS (Bikram Sambat) equivalent.
        /// </summary>
        public static (int Year, int Month, int Day) ADToBS(DateTime adDate)
        {
            if (adDate < AdEpoch)
                throw new ArgumentOutOfRangeException(nameof(adDate), $"Date is before the supported range (earliest supported: {AdEpoch:yyyy-MM-dd}).");

            int daysRemaining = (adDate.Date - AdEpoch).Days;

            int year = BsEpochYear;
            int month = 1;
            int day = 1 + daysRemaining;

            while (true)
            {
                if (!BSMonthData.TryGetValue(year, out int[] months))
                    throw new ArgumentOutOfRangeException(nameof(adDate), $"Date is after the supported range (no BS calendar data past year {MaxBsYear}).");

                int daysInMonth = months[month - 1];

                if (day <= daysInMonth)
                    return (year, month, day);

                day -= daysInMonth;
                month++;

                if (month > 12)
                {
                    month = 1;
                    year++;
                }
            }
        }

        /// <summary>
        /// Convenience overload returning the BS date as a "yyyy-MM-dd" string,
        /// matching the format already used elsewhere in this project
        /// (e.g. AttendanceDateNepali, RosterDateNepali columns).
        /// </summary>
        public static string ADToBSString(DateTime adDate)
        {
            var (year, month, day) = ADToBS(adDate);
            return $"{year:D4}-{month:D2}-{day:D2}";
        }

        /// <summary>
        /// Converts a BS (Bikram Sambat) date to its AD (Gregorian) equivalent.
        /// </summary>
        public static DateTime BSToAD(int bsYear, int bsMonth, int bsDay)
        {
            if (bsYear < MinBsYear || bsYear > MaxBsYear)
                throw new ArgumentOutOfRangeException(nameof(bsYear), $"BS year must be between {MinBsYear} and {MaxBsYear}.");

            if (bsMonth < 1 || bsMonth > 12)
                throw new ArgumentOutOfRangeException(nameof(bsMonth), "BS month must be between 1 and 12.");

            int[] targetYearMonths = BSMonthData[bsYear];

            if (bsDay < 1 || bsDay > targetYearMonths[bsMonth - 1])
                throw new ArgumentOutOfRangeException(nameof(bsDay), $"BS day must be between 1 and {targetYearMonths[bsMonth - 1]} for {bsYear}-{bsMonth:D2}.");

            int totalDays = 0;

            for (int y = BsEpochYear; y < bsYear; y++)
            {
                int[] yearMonths = BSMonthData[y];
                foreach (int d in yearMonths)
                    totalDays += d;
            }

            for (int m = 0; m < bsMonth - 1; m++)
                totalDays += targetYearMonths[m];

            totalDays += (bsDay - 1);

            return AdEpoch.AddDays(totalDays);
        }

        /// <summary>
        /// Parses a "yyyy-MM-dd" BS date string and converts it to AD.
        /// Returns null if the string is not a valid "yyyy-MM-dd" BS date.
        /// </summary>
        public static DateTime? BSStringToAD(string bsDateString)
        {
            if (string.IsNullOrWhiteSpace(bsDateString))
                return null;

            string[] parts = bsDateString.Split('-');
            if (parts.Length != 3)
                return null;

            if (!int.TryParse(parts[0], out int year) ||
                !int.TryParse(parts[1], out int month) ||
                !int.TryParse(parts[2], out int day))
                return null;

            try
            {
                return BSToAD(year, month, day);
            }
            catch (ArgumentOutOfRangeException)
            {
                return null;
            }
        }
